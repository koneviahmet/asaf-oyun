export function getAllTopics() {
  return [...new Set(questions.map((q) => q.topic))].sort()
}

export function getRandomQuestion(excludeIds = [], topicFilter = null) {
  let pool = questions.filter((q) => !excludeIds.includes(q.id))
  if (topicFilter?.length) {
    const filtered = pool.filter((q) => topicFilter.includes(q.topic))
    if (filtered.length > 0) pool = filtered
  }
  if (pool.length === 0) pool = questions.filter((q) => !excludeIds.includes(q.id))
  if (pool.length === 0) pool = questions
  return pool[Math.floor(Math.random() * pool.length)]
}

export function getTopicStats() {
  const map = {}
  questions.forEach((q) => {
    map[q.topic] = (map[q.topic] || 0) + 1
  })
  return Object.entries(map)
    .map(([topic, count]) => ({ topic, count }))
    .sort((a, b) => b.count - a.count)
}
